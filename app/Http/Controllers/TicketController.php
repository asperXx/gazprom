     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $ticket = new Ticket;
        $ticket->title = $request->title;
        $ticket->body = $request->body;
        $ticket->status = 0;
        $ticket->user_id = $request->user_id;

        $ticket->save();
    }

